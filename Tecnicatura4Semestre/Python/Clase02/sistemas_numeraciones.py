#profundizamos los sistamas de numeracion

#sistema decimal
a = 10
print(f"a decimal: {a}")

#sistema binario
a = 0b1010
print(f"a binario: {a}")

#sistema octal
a = 0o12
print(f"a octal: {a}")

#sistema hexadecimal
a = 0xA
print(f"a hexadecimal: {a}")

#base decimal
a = int("23", 10)
print(f"a = base decimal {a}")

#base binario
a = int("1010", 2)
print(f"a = base binario: {a}")

#base Octal
a = int("27", 8)
print(f"a = base octal: {a}")

# Base Hexadecimal
a = int('17', 16)
print(f"a = base hexadecimal: {a}")

# Base 5 sus valores son de 0 a 4
a = int('344', 5)
print(f"a = Base 5: {a}")
